def seed():
    """导入预置菜谱"""
    db = SessionLocal()
    try:
        # 检查是否已有系统菜谱
        existing = db.query(Recipe).filter(Recipe.source == "system").count()
        if existing > 0:
            print(f"已有 {existing} 道系统菜谱，跳过 seed")
            return

        all_recipes = RECIPES + EXTRA_RECIPES + EXTRA_RECIPES_2
        for data in all_recipes:
            recipe = Recipe(
                name=data["name"],
                description=data["description"],
                cook_time=data["cook_time"],
                servings=data["servings"],
                difficulty=data["difficulty"],
                category=data["category"],
                tips=data.get("tips"),
                source="system",
                is_public=True,
            )
            db.add(recipe)
            db.flush()

            for i, (name, amount, group) in enumerate(data["ingredients"]):
                db.add(RecipeIngredient(
                    recipe_id=recipe.id,
                    name=name,
                    amount=amount,
                    group_name=group,
                    sort_order=i,
                ))

            for i, (content, tip) in enumerate(data["steps"], 1):
                db.add(RecipeStep(
                    recipe_id=recipe.id,
                    step_number=i,
                    content=content,
                    tip=tip,
                ))

        db.commit()
        print(f"成功导入 {len(all_recipes)} 道预置菜谱")
    finally:
        db.close()


if __name__ == "__main__":
    seed()
